package operators;

public class Relational {
    public static void main(String[] args) {
        // Relational operator example
        int a=5;
        int b=10;
        System.out.println("Equal to operator (==) example a==b --->  "+ (a==b));//false
        System.out.println("Not equal to operator (!=) example a!=b --->  "+ (a!=b));//true
        System.out.println("Greater than operator (>) example a>b --->  "+ (a>b));//false
        System.out.println("Less than operator (<) example a<b --->  "+ (a<b));//true
        System.out.println("Greater than or Equal to operator (>=) example a>=b --->  "+ (a>=b));//false
        System.out.println("Less than or Equal to operator (<=) example a<=b --->  "+ (a<=b));//true
    }
}
